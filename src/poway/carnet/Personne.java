package poway.carnet;

public class Personne {
	
	
	public Personne(String nom, String prenom, String numero, String email) {
		super();
		this.nom = nom;
		this.prenom = prenom;
		this.numero = numero;
		this.email = email;
	}
	
	String nom;
	String prenom;
	String numero;
	String email;
}
